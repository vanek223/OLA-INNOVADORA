// Function to translate main page elements
function translateMainPage() {
    // All main page elements now have data-i18n attributes
    // so they are handled by updatePageContent() automatically
    
    // But we still need to translate elements that don't have data-i18n
    // like story features that are split across multiple spans
    
    // Translate stat labels that don't have data-i18n
    const statLabels = document.querySelectorAll('.stat-label:not([data-i18n])');
    if (statLabels.length > 0) {
        statLabels[0].textContent = t('statGuests');
    }
    if (statLabels.length > 1) {
        statLabels[1].textContent = t('statApartments');
    }
    if (statLabels.length > 2) {
        statLabels[2].textContent = t('statRating');
    }
    if (statLabels.length > 3) {
        statLabels[3].textContent = t('statSupport');
    }
}